const slider = document.querySelector(".sfondoIntro");
const sections = gsap.utils.toArray(".slider section");

let tl = gsap.timeline({
  defaults: {
    ease: "none",
  },
  scrollTrigger: {
    trigger: slider,
    start: "top top",
    pin: true,
    scrub: 2,
    markers: true,
    end: () => "+=" + slider.offsetWidth,
  },
});

tl.to(slider, {
  xPercent: -83.4,
});
